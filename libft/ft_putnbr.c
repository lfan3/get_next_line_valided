#include "libft.h"

void	ft_putnbr(int n)
{
	long	numb;

	numb = n;
	if (numb < 0)
	{
		ft_putchar(45);
		numb = -numb;
	}
	if (numb > 9)
		ft_putnbr(numb / 10);
	ft_putchar(numb % 10 + '0');
}
